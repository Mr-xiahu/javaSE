package com.xiahu.Integer;

public class Demo {
	public static void main(String[] args) {
		// public static String toBinaryString(int i)八进制
		System.out.println(Integer.toBinaryString(100));//1100100
		// public static String toOctalString(int i)
		System.out.println(Integer.toOctalString(100));//144
		// public static String toHexString(int i)
		System.out.println(Integer.toHexString(100));//64

		// public static final int MAX_VALUE
		System.out.println(Integer.MAX_VALUE);//2147483647  最大值
		// public static final int MIN_VALUE
		System.out.println(Integer.MIN_VALUE);//-2147483648   最小值
	}

}
